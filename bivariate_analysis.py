import pandas as pd
import numpy as np
import matplotlib.pyplot as plt
import seaborn as sns

df = pd.read_csv("cleaned_weatherAUS.csv")

import plotly.express as px
###Comparison Temp and Rain fall
fig = px.scatter(df, x='Temp9am', y='Temp3pm', color='Rainfall', hover_data=['Temp9am', 'Temp3pm', 'Rainfall'])

fig.update_layout( title="Comparison Temp and Rain fall")

fig.show()

###Comparison Pressure and Rain fall
fig = px.scatter(df, x='Pressure9am', y='Pressure3pm', color='Rainfall',hover_data=['Pressure9am', 'Pressure3pm', 'Rainfall'])

fig.update_layout(
    title="Comparison Pressure and Rain fall")
fig.show()

# Dividing the wind speed into 5 packages
df['Wind gust Speed Group'] = pd.cut(df['WindGustSpeed'], bins=np.arange(0, 75, 5), right=False)

# Create a hexbin diagram
plt.figure(figsize=(12, 6))
hb = plt.hexbin(df['WindGustSpeed'], df['Rainfall'], gridsize=20, cmap='pink')

plt.title('Effect of Wind Gust Speed on Rainfall')
plt.xlabel('Wind speed (km/h)')
plt.ylabel('Rain fall')
plt.colorbar(label='number of data')
plt.grid(True)
plt.show()

sns.lineplot(data=df,x='Sunshine',y='Rainfall',color='green')

sns.lineplot(data=df,x='Sunshine',y='Evaporation',color='blue')

plt.figure(figsize=(8, 8))
sns.heatmap(df.corr(), annot=True, cmap='coolwarm', fmt='.1f')
plt.title('Correlation Heatmap')
plt.show()